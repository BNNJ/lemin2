#include "libft.h"

void	ft_putb_int(int nb)
{
	char	i;

	i = 32;
	while (i--)
	{
		write(1, (nb & (1 << i)) ? "1" : "0", 1);
		if (i % 8 == 0 && i != 0)
			write(1, " ", 1);
	}
}
