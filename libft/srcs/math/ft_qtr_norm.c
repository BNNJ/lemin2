#include "libft.h"

void	ft_qtr_norm(t_qtr *q)
{
	double	len;

	len = ft_qtr_len(q);
	if (len >= 0.0001f)
	{
		q->w /= len;
		q->v.x /= len;
		q->v.y /= len;
		q->v.z /= len;
	}
}
