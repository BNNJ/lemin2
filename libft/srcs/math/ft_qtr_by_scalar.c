#include "libft.h"

t_qtr	ft_qtr_by_scalar(const t_qtr *q1, const double s)
{
	t_qtr	q2;

	q2 = ft_qtr_new(0, 0, 0, s);
	return (ft_qtr_mult(q1, &q2));
}
