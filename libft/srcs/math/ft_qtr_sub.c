#include "libft.h"

t_qtr	ft_qtr_sub(const t_qtr *q1, const t_qtr *q2)
{
	t_qtr	ret;

	ret.w = q1->w - q2->w;
	ret.v = ft_vec3_sub(&q1->v, &q2->v);
	return (ret);
}
