#include "libft.h"

t_mat4	ft_mat4_trslt(double x, double y, double z)
{
	t_mat4	m;

	m.x = (t_vec4){1.0, 0.0, 0.0, 0.0};
	m.y = (t_vec4){0.0, 1.0, 0.0, 0.0};
	m.z = (t_vec4){0.0, 0.0, 1.0, 0.0};
	m.w = (t_vec4){x, y, z, 0.0};
	return (m);
}
