#include "libft.h"

t_vec3	ft_vec3_add(const t_vec3 *v1, const t_vec3 *v2)
{
	t_vec3	v;

	v.x = v1->x + v2->x;
	v.x = v1->y + v2->y;
	v.x = v1->z + v2->z;
	return (v);
}
