#include "libft.h"

char	*ft_itoa(int n)
{
	char	str[12];
	int		i;

	i = 10;
	str[11] = '\0';
	while (n)
	{
		str[i] = n >= 0 ? (n % 10) + '0' : -(n % 10) + '0';
		str[i - 1] = n >= 0 ? '0' : '-';
		n /= 10;
		--i;
	}
	if (str[i + 1] == '\0')
		str[i] = '0';
	else if (str[i] == '0')
		++i;
	return (ft_strdup(str + i));
}
